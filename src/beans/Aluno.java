package beans;

public class Aluno {
	private Integer alu_identificador;
	private String alu_nome;
	private Integer alu_tur_identificador;
	
	public Integer getAlu_identificador() {
		return alu_identificador;
	}
	public void setAlu_identificador(Integer alu_identificador) {
		this.alu_identificador = alu_identificador;
	}
	public String getAlu_nome() {
		return alu_nome;
	}
	public void setAlu_nome(String alu_nome) {
		this.alu_nome = alu_nome;
	}
	public Integer getAlu_tur_identificador() {
		return alu_tur_identificador;
	}
	public void setAlu_tur_identificador(Integer alu_tur_identificador) {
		this.alu_tur_identificador = alu_tur_identificador;
	}
	
		
}
