package beans;

public class Professor {
	private int pro_identificador;
	private String pro_nome;
	private String pro_titulacao;
	
	public int getPro_identificador() {
		return pro_identificador;
	}
	public void setPro_identificador(int pro_identificador) {
		this.pro_identificador = pro_identificador;
	}
	public String getPro_nome() {
		return pro_nome;
	}
	public void setPro_nome(String pro_nome) {
		this.pro_nome = pro_nome;
	}
	public String getPro_titulacao() {
		return pro_titulacao;
	}
	public void setPro_titulacao(String pro_titulacao) {
		this.pro_titulacao = pro_titulacao;
	}
		
}
