package beans;

public class Curso {
	private int cur_identificador;
	private String cur_nome;
	private String cur_periodo;
	private String cur_descricao;
	
	public int getCur_identificador() {
		return cur_identificador;
	}
	public void setCur_identificador(int cur_identificador) {
		this.cur_identificador = cur_identificador;
	}
	public String getCur_nome() {
		return cur_nome;
	}
	public void setCur_nome(String cur_nome) {
		this.cur_nome = cur_nome;
	}
	public String getCur_periodo() {
		return cur_periodo;
	}
	public void setCur_periodo(String cur_periodo) {
		this.cur_periodo = cur_periodo;
	}
	public String getCur_descricao() {
		return cur_descricao;
	}
	public void setCur_descricao(String cur_descricao) {
		this.cur_descricao = cur_descricao;
	}
	
	
}
