package beans;

public class Turma {
	private int tur_identificador;
	private String tur_descricao;
	private int tur_cur_identificador;
	
	public int getTur_identificador() {
		return tur_identificador;
	}
	public void setTur_identificador(int tur_identificador) {
		this.tur_identificador = tur_identificador;
	}
	public String getTur_descricao() {
		return tur_descricao;
	}
	public void setTur_descricao(String tur_descricao) {
		this.tur_descricao = tur_descricao;
	}
	public int getTur_cur_identificador() {
		return tur_cur_identificador;
	}
	public void setTur_cur_identificador(int tur_cur_identificador) {
		this.tur_cur_identificador = tur_cur_identificador;
	}		
}
